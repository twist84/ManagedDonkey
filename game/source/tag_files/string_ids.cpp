void __cdecl string_replace_character(char* buffer, char find, char replace)
{
	while (true)
	{
		char* found = strchr(buffer, find);
		if (!found)
			break;
		*found = replace;
		buffer = found + 1;
	}
}

void __cdecl string_id_convert_static_string(c_static_string<128>* static_string)
{
	ascii_strnlwr(static_string->get_buffer(), 128);
	string_replace_character(static_string->get_buffer(), ' ', '_');
	string_replace_character(static_string->get_buffer(), '-', '_');
}

